
public class Student {
private String name;//Encapsulation is making variable private and 
//using getters and setters to pass and call the value
private int Age;

public void setName(String name) {
	this.name=name;
}
public String getName() {
	return name;
}
public void setAge(int Age) {
	this.Age=Age;
}
public int getAge() {
	return Age;
}
}